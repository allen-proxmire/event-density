"""
triad_cascade.py
=================
Experiment: Triad Cascade Dynamics (Atlas §4, Suite §3)

Investigates the spectral cascade generated by nonlinear triad interactions
in the canonical ED system. Seeds specific pairs and clusters of modes at
small amplitude (weakly nonlinear regime) and tracks the energy redistribution
across the modal hierarchy.

Initial conditions:
  IC1: modes {1, 2}            — fundamental forward cascade (generates 3, 1)
  IC2: modes {2, 3}            — forward + inverse cascade (generates 5, 1)
  IC3: modes {1, 3}            — gap triad (generates 4, 2)
  IC4: modes {1, 2, 3}         — broadband triad cluster

Uses Parameter Set I (deep oscillatory, same as modal_hierarchy.py and
three_stage_convergence.py).

Output directories:
  output/runs/triad_cascade_IC1
  output/runs/triad_cascade_IC2
  output/runs/triad_cascade_IC3
  output/runs/triad_cascade_IC4

Usage:
    python experiments/triad_cascade.py

All notation follows Appendix C of the Rigour Paper.
"""

import os
import sys
import json
import time as _time
import numpy as np

# Allow imports from the parent ED Simulation directory
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from edsim_parameters import load_parameter_set
from edsim_runner import (
    RunConfig, TimeSeries, ensure_dirs,
    diagnostic_snapshot, enforce_bounds,
)
from edsim_core import make_grid, step


# ---------------------------------------------------------------------------
#  Configuration
# ---------------------------------------------------------------------------

PARAM_SET = "I"
A = 0.05                 # Per-mode amplitude (weakly nonlinear regime)
T = 15.0                 # Final time (sufficient for cascade development)
METHOD = "crank_nicolson"
K_OUT = 10               # Output every K_OUT steps (high-resolution time series)
N_OBS = 32               # Track 32 modal amplitudes

# Initial conditions: dict of {label: list of seeded mode numbers}
IC_SPECS = {
    "IC1": [1, 2],
    "IC2": [2, 3],
    "IC3": [1, 3],
    "IC4": [1, 2, 3],
}


# ---------------------------------------------------------------------------
#  Helpers
# ---------------------------------------------------------------------------

def build_multimode_ic(x: np.ndarray, params, modes: list, A_per_mode: float):
    """Construct an initial density field by summing Neumann eigenmodes.

    rho(x, 0) = rho* + A_per_mode * sum_{n in modes} cos(n * pi * x / L)
    v(0) = 0

    The amplitude is divided equally among modes so that the total
    perturbation energy scales with the number of modes but each
    individual mode stays in the weakly nonlinear regime.
    """
    rho0 = np.full_like(x, params.rho_star)
    for n in modes:
        rho0 += A_per_mode * np.cos(n * np.pi * x / params.L)

    # Clamp to admissible region (should not trigger at A = 0.05)
    rho0 = np.clip(rho0, 1e-12, params.rho_max - 1e-12)

    v0 = 0.0
    return rho0, v0


def run_single_ic(label: str, modes: list, verbose: bool = True) -> dict:
    """Run a single triad-cascade experiment.

    Parameters
    ----------
    label : IC label string ("IC1", "IC2", etc.)
    modes : list of mode numbers to seed
    verbose : print progress

    Returns
    -------
    dict with keys: time_series (dict of arrays), metadata (dict)
    """
    run_id = f"triad_cascade_{label}"
    if verbose:
        print(f"\n{'='*60}")
        print(f"  {run_id}: seeded modes = {modes}, A = {A}")
        print(f"{'='*60}")

    # Load parameters — same canonical Set I as other experiments
    params = load_parameter_set(PARAM_SET, T=T, method=METHOD, k_out=K_OUT)

    # Build initial condition
    x = make_grid(params)
    rho0, v0 = build_multimode_ic(x, params, modes, A)

    # Prepare output directory
    out_dir = os.path.join(
        os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
        "output", "runs", run_id,
    )
    # Use the centralized ensure_dirs for the top-level structure,
    # then create our specific run directory
    ensure_dirs()
    os.makedirs(out_dir, exist_ok=True)

    # --- Integration loop (mirrors triad_coupling.py) ---
    t_val = 0.0
    rho = rho0.copy()
    v = v0
    N_steps = int(np.ceil(params.T / params.dt))

    ts = TimeSeries(N_obs=N_OBS)
    snap0 = diagnostic_snapshot(rho, v, t_val, params, N_OBS)
    ts.append(snap0)

    wall_start = _time.perf_counter()
    n_pos_violations = 0
    n_cap_violations = 0

    for step_n in range(1, N_steps + 1):
        t_val = step_n * params.dt
        res = step(rho, v, params)
        rho = res["rho"]
        v = res["v"]

        # Enforce bounds (defensive)
        rho, pos_flag, cap_flag = enforce_bounds(rho, params)
        if pos_flag:
            n_pos_violations += 1
        if cap_flag:
            n_cap_violations += 1

        # Record diagnostics at output cadence
        if step_n % params.k_out == 0 or step_n == N_steps:
            snap = diagnostic_snapshot(rho, v, t_val, params, N_OBS)
            ts.append(snap)

        # Progress reporting
        if verbose and step_n % (N_steps // 10 or 1) == 0:
            pct = 100.0 * step_n / N_steps
            print(f"    t = {t_val:.4f}  ({pct:.0f}%)")

    wall_time = _time.perf_counter() - wall_start
    ts_arrays = ts.to_arrays()

    # --- Save time_series.npz ---
    ts_path = os.path.join(out_dir, "time_series.npz")
    np.savez_compressed(ts_path, **ts_arrays)
    if verbose:
        print(f"  Saved: {ts_path}")

    # --- Save final_state.npz ---
    final_path = os.path.join(out_dir, "final_state.npz")
    np.savez_compressed(
        final_path,
        rho=rho,
        v=np.array([v]),
        t=np.array([t_val]),
    )

    # --- Save metadata.json ---
    metadata = {
        "run_id": run_id,
        "experiment": "triad_cascade",
        "ic_label": label,
        "seeded_modes": modes,
        "A_per_mode": A,
        "param_set": PARAM_SET,
        "D": params.D,
        "zeta": params.zeta,
        "tau": params.tau,
        "rho_star": params.rho_star,
        "rho_max": params.rho_max,
        "M0": params.M0,
        "beta": params.beta,
        "P0": params.P0,
        "N": params.N,
        "L": params.L,
        "dt": params.dt,
        "T": params.T,
        "method": params.method,
        "k_out": params.k_out,
        "N_obs": N_OBS,
        "n_steps": N_steps,
        "final_t": t_val,
        "wall_time_s": wall_time,
        "n_positivity_violations": n_pos_violations,
        "n_capacity_violations": n_cap_violations,
        "termination_reason": "FinalTime",
        "ic_name": "custom_multimode",
        "ic_kwargs": {"modes": modes, "A": A},
        # Predicted triad targets for reference
        "predicted_targets": _predicted_targets(modes),
        "timestamp": _time.strftime("%Y-%m-%dT%H:%M:%SZ", _time.gmtime()),
    }

    meta_path = os.path.join(out_dir, "metadata.json")
    with open(meta_path, "w") as f:
        json.dump(metadata, f, indent=2, default=_json_default)
    if verbose:
        print(f"  Saved: {meta_path}")

    # --- Summary ---
    if verbose:
        E_init = ts_arrays["E_total"][0]
        E_final = ts_arrays["E_total"][-1]
        C_init = ts_arrays["C_ED"][0]
        C_final = ts_arrays["C_ED"][-1]
        print(f"\n  Summary:")
        print(f"    Wall time:      {wall_time:.2f} s")
        print(f"    Steps:          {N_steps}")
        print(f"    E(0) = {E_init:.6e},  E(T) = {E_final:.6e}")
        print(f"    C_ED(0) = {C_init:.6e},  C_ED(T) = {C_final:.6e}")
        print(f"    Positivity violations: {n_pos_violations}")
        print(f"    Capacity violations:   {n_cap_violations}")
        print(f"    Predicted targets:     {metadata['predicted_targets']}")

    return {
        "time_series": ts_arrays,
        "metadata": metadata,
    }


def _predicted_targets(modes: list) -> dict:
    """Compute all triad-predicted target modes from the seeded set.

    For each pair (m, n) with m != n, the triad selection rule
    (Theorem C.34) predicts activation of modes m+n and |m-n|.
    For m == n, it predicts 2m and 0.

    Returns dict mapping source pair "(m,n)" -> list of target modes.
    """
    targets = {}
    for i, m in enumerate(modes):
        for j, n in enumerate(modes):
            if j <= i:
                continue
            pair_key = f"({m},{n})"
            if m == n:
                targets[pair_key] = sorted({2 * m, 0})
            else:
                targets[pair_key] = sorted({m + n, abs(m - n)})
    return targets


def _json_default(obj):
    """JSON serializer for numpy types."""
    if isinstance(obj, (np.integer,)):
        return int(obj)
    if isinstance(obj, (np.floating,)):
        return float(obj)
    if isinstance(obj, np.ndarray):
        return obj.tolist()
    if isinstance(obj, set):
        return sorted(obj)
    raise TypeError(f"Object of type {type(obj)} is not JSON serializable")


# ---------------------------------------------------------------------------
#  Main
# ---------------------------------------------------------------------------

def main():
    print("=" * 60)
    print("  Triad Cascade Experiment")
    print(f"  Parameter Set {PARAM_SET}, A = {A}, T = {T}")
    print("=" * 60)

    results = {}
    for label in sorted(IC_SPECS.keys()):
        modes = IC_SPECS[label]
        results[label] = run_single_ic(label, modes, verbose=True)

    # --- Cross-IC summary ---
    print("\n" + "=" * 60)
    print("  Cross-IC Summary")
    print("=" * 60)
    print(f"  {'IC':<6} {'Modes':<16} {'E(0)':<14} {'E(T)':<14} "
          f"{'C_ED(0)':<14} {'C_ED(T)':<14}")
    print("  " + "-" * 78)

    for label in sorted(results.keys()):
        ts = results[label]["time_series"]
        modes = IC_SPECS[label]
        print(f"  {label:<6} {str(modes):<16} "
              f"{ts['E_total'][0]:<14.6e} {ts['E_total'][-1]:<14.6e} "
              f"{ts['C_ED'][0]:<14.6e} {ts['C_ED'][-1]:<14.6e}")

    print(f"\nAll runs saved to: output/runs/triad_cascade_IC{{1,2,3,4}}")
    print("Done.")


if __name__ == "__main__":
    main()
